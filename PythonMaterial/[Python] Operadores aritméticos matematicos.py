# Tipos de variáveis matematicas
a= 2
b = 5
print('a = {} e b = {}'.format(a,b))

# Adição
print('Soma:',a+b)

# Subtração
print('Subtração:',a-b) # Resultados negativos podem ocorrer normalmente

# Multiplicação
print('Multiplicação:',a*b)

# Divisão
print('Divisão:',a/b)

# Resto da divisão
print('Resto da divisão:',a%b) # esta operação retorna o valor inteiro do resto da divisão 5 dividido por 2 gera resto inteiro 1

# Valor inteiro da divisão
print('Divisão resto inteiro:',a//b) # Retorna o valor inteiro da divisão

# Exponenciação
print('Exponenciação a e b:',a**b)

# Operações matematica respeitam as normas da matematica, matematica, divisão, soma e subtração