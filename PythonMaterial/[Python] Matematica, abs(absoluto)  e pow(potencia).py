# Valores absolutos e potencias

# Modelo de uso do comando ABS que gera o valor absoluto trazendo o valor positivo.
print('Modelo de uso do comando ABS para gerar um numero positivo.')
x = abs(-7.25)
print('Utilizando o comando ABS no valor negativo -7.25 temos como resultado:',x)
print('Termino de excecução do modelo\n')

# Modelo de uso do comando POW para gerar uma potencia de um valor
print('Modelo de uso do comando POW para realizar o processo de potencialização de um numero.')
y = pow(3,3) # O primeiro numero é a base e o segundo numero é o expoente
print('Utilizando o comando POW para gerar a potencia de 3^3, temos:',y)
print('Termino de excecução do modelo')