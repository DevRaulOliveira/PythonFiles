# Operadores de comparação retornam resultados booleanos
# Resultados booleanos são baseados em verdadeiro e falso
# Podendo retornar 0 ou 1

# Comparação de igualdade usando ==
a= 1; b= 1; 
print('O valor de a é {} e b tem o valor de {}'.format(a,b),'\n')
print('O valor de a é igual ao valor de b?',a==b)
print('O valor de  a é diferente do valor de b?',a!=b)
print('O valor de a é menor que o valor de b?', a<b)
print('O valor de a é menor ou igual a b?',a<=b)