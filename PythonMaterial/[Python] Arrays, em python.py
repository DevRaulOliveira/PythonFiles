# Criação de Arrays em Python
# Python não tem suporte a arrays
# Matriz são utilizadas para guardar diversos valores em uma unica variável 
# Colções de dados são matrizes de dados

# Exemplo de Matriz
cars = ['Ford','Volvo','BMW'] # Uma lista pode ser interpretada como uma matriz por guardar diversos valores em uma unica variável

# Interagindo com um unico item da matriz
# É importante citar que essa interação é da mesma forma no qual selecionamentos o indice de listas, tuplas e sets
a =cars[0]
print('Interagindo com um unico item da matriz.')
print('Selecionando o primeiro item da lista(matriz) utilizando o indice entre conchetes e atribuindo a uma variável temos:\n{}\n'.format(a))
print('Fim da execução.')

# Imprimindo um comprimento de uma lista
print('Usando o comando print(len(cars)) temos:\n{}'.format(len(cars)))