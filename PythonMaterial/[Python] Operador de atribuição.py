# Operador de atribuição
x = 5
print('Este é o valor da variável x:\n',x)


y = x+3
# Atribuindo um valor a uma variável
print('A variável y recebe o valor da soma de x+3:\n',y)

x+=3 # A partir dessa linha x recebe o valor de 5 +3
print('\nA variável x mudou de valor a partir dessa linha por causa do calculox+=3:',x)

x-=3 # O valor de x apartir dessa linha perdeu 3 unidades voltando a ser 5
print('\nA operação x-=3 resultou em:',x)

# O mesmos se aplica as outras operações básicas.

# Atribuindo a variável o valor do resto da divisão
x%=3
print('\nA variável x recebendo a soma do valor do resto da divisão resultou em:',x) # o resto da divisão de 5 por 3 é 2

