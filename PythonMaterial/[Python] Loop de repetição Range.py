# Utilizando o loop de repetição range
# Utilizando o range é possível definir o momento de início do FOR

# Modelo de uso do RANGE
for x in range(6): # O range determina a quantiade de vezes que o for será executado
    print(x)
print('Fim do programa.\n')

# Modelo determinando o início e o fim da execução do for com o RANGE
for y in range(1,8): # Significa que a execução começa no 1 e termina no 7
    print(y)
print('Fim do programa.\n')

# Utilizando outro valor de incremento no RANGE
for z in range(0,8,2): # O ultimo valor é usado para definir o incremento, ou seja a progressão de valor
    print(z)
print('Fim do programa.')